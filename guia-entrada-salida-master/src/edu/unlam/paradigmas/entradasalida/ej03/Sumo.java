package edu.unlam.paradigmas.entradasalida.ej03;

public class Sumo {

	private int peso;
	private int altura;

	public Sumo(int peso, int altura) {
		this.peso = peso;
		this.altura = altura;
	}

	public int getPeso() {
		return peso;
	}

	public double getAltura() {
		return altura;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

	public void setAltura(int altura) {
		this.altura = altura;
	}

}
